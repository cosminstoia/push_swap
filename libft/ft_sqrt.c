#include "libft.h"

int	ft_sqrt(int nbr)
{
	int	i;

	i = 0;
	if (nbr < 4)
		return (1);
	while (i * i <= nbr)
	{
		if (i * i == nbr)
			return (i);
		i++;
	}
	return (i - 1);
}
